def generate_sample_word():
    """生成示例 Word 文件"""
    from docx import Document

    doc = Document()
    doc.add_heading('教师信息登记表', 0)

    # 添加表格形式的教师信息
    table = doc.add_table(rows=1, cols=8)
    table.style = 'Table Grid'
    headers = ['姓名', '性别', '身份证号', '手机', '学历', '毕业院校', '政治面貌', '任教学科']
    for i, h in enumerate(headers):
        table.rows[0].cells[i].text = h

    teachers = [
        ['张明远', '男', '370983198509121234', '13700007001', '硕士', '北京大学', '中共党员', '物理'],
        ['王丽华', '女', '370983199112082345', '13700007002', '本科', '山东大学', '共青团员', '语文'],
        ['李建国', '男', '370983197805153456', '13700007003', '本科', '曲阜师范大学', '中共党员', '数学'],
    ]
    for t in teachers:
        row = table.add_row()
        for i, val in enumerate(t):
            row.cells[i].text = val

    filepath = os.path.join(os.path.dirname(__file__), "test_data", "sample_teacher_info.docx")
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    doc.save(filepath)
    print(f"✅ 已生成: {filepath}")


